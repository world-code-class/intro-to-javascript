// Try writing a for loop that iterates 5 times, and each time
// logs out the index

// Solution: We iterate 5 times, and since we start from 0, we will
// go until i is greater than 4, or less than 5

// A good way to remember is to start the index at 0, and let it 
// go for how many times you want the loop to run

for(var i = 0; i < 5; i++) {
	// i is the current iteration of the for loop
	console.log(i)
}


