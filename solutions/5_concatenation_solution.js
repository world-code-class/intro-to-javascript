// Now we're going to talk about string concatenation
// Concatenation is just a fancy word for "combining two strings"
// 
// To concatenate two strings, just use the + sign, like with addition
// 
// When we combine these strings, the string on the right is added to the
// string on the left
// 
// Now let's try some examples
// 
// Try adding the strings "Hello" and "World"
console.log("Hello" + "World")
// 
// Now try adding a space between the two words
console.log("Hello" + " " + "World")